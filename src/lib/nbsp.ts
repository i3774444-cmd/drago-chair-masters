// Заменяет обычные пробелы на неразрывные после коротких слов
// (предлогов, союзов, частиц) и между числом и следующим словом.
const SHORT_WORDS = [
  "а", "и", "о", "у", "к", "с", "в", "по", "на", "до", "от", "из",
  "за", "об", "не", "ни", "но", "же", "ли", "бы", "то", "под", "над",
  "для", "при", "без", "как", "что", "это", "год", "год.", "лет",
  "BYN", "руб", "руб.", "₽", "$",
];

export function nbsp(input: string): string {
  let s = input;

  // Короткие слова (1-3 буквы) + пробел → слово + nbsp
  // Сначала: одиночные/двух/трёхбуквенные предлоги в начале или после пробела
  s = s.replace(
    /(^|[\s(«"])([аиоукcвсАИОУКСВ]|[А-Яа-я]{2,3})\s+/g,
    (m, p1, p2) => {
      const lower = p2.toLowerCase();
      if (SHORT_WORDS.includes(lower)) return `${p1}${p2}\u00A0`;
      return m;
    },
  );

  // Число + пробел + слово → число + nbsp + слово
  s = s.replace(/(\d+[+%]?)\s+([А-Яа-яA-Za-z])/g, "$1\u00A0$2");

  // "от/до/с/по" перед числом
  s = s.replace(/\b(от|до|с|по|за)\s+(\d)/giu, "$1\u00A0$2");

  // Тире с пробелами → не разрываем перед тире
  s = s.replace(/\s—\s/g, "\u00A0— ");

  return s;
}
